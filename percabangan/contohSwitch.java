public class contohSwitch {
    public static void main (String[] args){
        int pilihan = 1;
        switch(pilihan){
            case 1: System.out.println("Soto Ayam"); break;
            case 2: System.out.println("Gule Kambing"); break;
            case 3: System.out.println("Nasi goreng"); break;
            default: System.out.println("Silahkan pilih 1,2 atau 3");
        }
                
    }
}
